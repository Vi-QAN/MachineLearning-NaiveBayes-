public class StudentDetail {
    private boolean isMale;
    private boolean ownBusiness;
    private boolean hasPartime;
    private boolean ruralArea;
    private boolean studyBusiness;
    private boolean becomeEntrepreneur;

    public boolean getIsMale() {
        return this.isMale;
    }

    public void setIsMale(boolean isMale) {
        this.isMale = isMale;
    }

    public boolean getOwnBusiness() {
        return this.ownBusiness;
    }

    public void setOwnBusiness(boolean ownBusiness) {
        this.ownBusiness = ownBusiness;
    }

    public boolean getHasPartime() {
        return this.hasPartime;
    }

    public void setHasPartime(boolean hasPartime) {
        this.hasPartime = hasPartime;
    }

    public boolean getRuralArea() {
        return this.ruralArea;
    }

    public void setRuralArea(boolean ruralArea) {
        this.ruralArea = ruralArea;
    }

    public boolean getStudyBusiness() {
        return this.studyBusiness;
    }

    public void setStudyBusiness(boolean studyBusiness) {
        this.studyBusiness = studyBusiness;
    }


    public boolean getBecomeEntrepreneur() {
        return this.becomeEntrepreneur;
    }

    public void setBecomeEntrepreneur(boolean becomeEntrepreneur) {
        this.becomeEntrepreneur = becomeEntrepreneur;
    }

    
}